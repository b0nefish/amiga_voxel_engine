// aaab bccc

void GenerateColorBytesDither3x2()
{
	UWORD address;
	UBYTE a1,a2,b1,b2,c1,c2;
	for(int a=0;a<COLORS;a++)
	for(int b=0;b<COLORS;b++)
	for(int c=0;c<COLORS;c++)
	{
		address = (a<<10) + (b<<5) + c;

		if(a % 2)
		{
			a1 = a/2;
			a2 = a/2;
		}
		else
		{
			a1 = a/2;
			a2 = a/2-1;
		}
		if(b % 2)
		{
			b1 = b/2;
			b2 = b/2;
		}
		else
		{
			b1 = b/2;
			b2 = b/2-1;
		}
		if(c % 2)
		{
			c1 = c/2;
			c2 = c/2;
		}
		else
		{
			c1 = c/2;
			c2 = c/2-1;
		}
		engine.renderer.ditherTable1[address]=
		((a1>>0) & 1) *0b10000000+
		((a2>>0) & 1) *0b01000000+
		((a1>>0) & 1) *0b00100000+
		((b2>>0) & 1) *0b00010000+
		((b1>>0) & 1) *0b00001000+
		((c2>>0) & 1) *0b00000100+
		((c1>>0) & 1) *0b00000010+
		((c2>>0) & 1) *0b00000001;

		engine.renderer.ditherTable2[address]=
		((a1>>1) & 1) *0b10000000+
		((a2>>1) & 1) *0b01000000+
		((a1>>1) & 1) *0b00100000+
		((b2>>1) & 1) *0b00010000+
		((b1>>1) & 1) *0b00001000+
		((c2>>1) & 1) *0b00000100+
		((c1>>1) & 1) *0b00000010+
		((c2>>1) & 1) *0b00000001;

		engine.renderer.ditherTable3[address]=
		((a1>>2) & 1) *0b10000000+
		((a2>>2) & 1) *0b01000000+
		((a1>>2) & 1) *0b00100000+
		((b2>>2) & 1) *0b00010000+
		((b1>>2) & 1) *0b00001000+
		((c2>>2) & 1) *0b00000100+
		((c1>>2) & 1) *0b00000010+
		((c2>>2) & 1) *0b00000001;

		engine.renderer.ditherTable4[address]=
		((a1>>3) & 1) *0b10000000+
		((a2>>3) & 1) *0b01000000+
		((a1>>3) & 1) *0b00100000+
		((b2>>3) & 1) *0b00010000+
		((b1>>3) & 1) *0b00001000+
		((c2>>3) & 1) *0b00000100+
		((c1>>3) & 1) *0b00000010+
		((c2>>3) & 1) *0b00000001;


	}
}
